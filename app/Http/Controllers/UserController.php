    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $inputs = $request->all();

        $signed_user = User::where('email',$request->get('email'))->get();

        if(count($signed_user)>0){
            $data = ApiRes::$error;
            $data['message'] = "email already used";
            return response()->json($data);
        }

        if($user = User::create($inputs))
        {
            $data = ApiRes::$success;
            $data["data"] = User::find($user)->toArray();
            return response()->json($data);
        }

        else
        {
            return json_encode(["status"=>"error", "message"=>"signup error"]);
        }

    }

